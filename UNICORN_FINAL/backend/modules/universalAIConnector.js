// ─── TASK TYPE → preferred capabilities map ──────────────────────────────────
const TASK_ROUTING = {
  coding:     ['coding', 'reasoning', 'general'],
  reasoning:  ['reasoning', 'general'],
  search:     ['search', 'reasoning', 'general'],
  embeddings: ['embeddings', 'general'],
  fast:       ['fast', 'general'],
  simple:     null,  // cheapest available
  complex:    null,  // highest accuracy
  general:    null,  // cheapest available
};

class UniversalAIConnector {
  constructor() {
    // Cache cu TTL per intrare; max 200 intrări
    this.cache = new Map();
    this.cacheTTL = parseInt(process.env.UAIC_CACHE_TTL_MS || '120000', 10);
    this.cacheMaxSize = 200;
    // Modele active (cheie validă în .env)
    this.models = [];
    this.stats = {
      totalCalls: 0,
      cacheHits: 0,
      callsByModel: {},
      errors: {},
      latencyMs: {},
    };
    this._loadActiveModels();
    // Auto-reload la fiecare 10 minute (preia chei noi din .env dacă sunt injectate runtime)
    this._reloadTimer = setInterval(() => this._loadActiveModels(), 600000);
    if (this._reloadTimer.unref) this._reloadTimer.unref();
  }

  // ─── Construiește lista de modele disponibile (cheie prezentă și validă în .env)
  _loadActiveModels() {
    this.models = PROVIDER_CONFIGS.filter(cfg => {
      const key = process.env[cfg.apiKeyEnv];
      return key && key.length > 10 && !key.startsWith('your_') && !key.startsWith('sk-your');
    }).map(cfg => ({
      ...cfg,
      apiKey: process.env[cfg.apiKeyEnv],
      // rezolvă endpoint dinamic (Gemini are modelul în URL)
      endpoint: cfg.name === 'gemini'
        ? `https://generativelanguage.googleapis.com/v1beta/models/${process.env.GEMINI_MODEL || cfg.model}:generateContent`
        : cfg.endpoint,
    }));
    console.log(`[UAIC] Modele active (${this.models.length}): ${this.models.map(m => m.name).join(', ') || 'niciun model – setează cheile API'}`);
  }

  // ─── Returnează modelele disponibile (fără cheile API)
  getModels() {
    return this.models.map(({ apiKey, ...rest }) => rest); // eslint-disable-line no-unused-vars
  }

  getStatus() {
    return {
      active: this.models.length > 0,
      modelsCount: this.models.length,
      models: this.getModels(),
      stats: this.stats,
    };
  }

  getStats() {
    return { ...this.stats, availableModels: this.models.length };
  }

  // ─── Selectează cel mai bun model pentru tipul de sarcină
  // taskType: 'simple'|'complex'|'fast'|'coding'|'reasoning'|'search'|'embeddings'|'general'
  selectModel(taskType = 'simple', excludeNames = []) {
    const candidates = this.models.filter(m => !excludeNames.includes(m.name));
    if (candidates.length === 0) return null;

    // Pentru task-uri cu routing explicit → filtrează după capability, apoi cheapest
    const preferredCaps = TASK_ROUTING[taskType];
    if (preferredCaps) {
      const matched = candidates.filter(m =>
        m.capabilities && m.capabilities.some(c => preferredCaps.includes(c))
      );
      const pool = matched.length > 0 ? matched : candidates;

      if (taskType === 'fast') {
        return [...pool].sort((a, b) => b.performance.speed - a.performance.speed)[0];
      }
      if (taskType === 'complex') {
        return [...pool].sort((a, b) => b.performance.accuracy - a.performance.accuracy)[0];
      }
      // coding / reasoning / search / embeddings → cheapest din cei potriviți
      return [...pool].sort((a, b) => a.cost - b.cost)[0];
    }

    // 'simple' / 'general' / default → cel mai ieftin
    if (taskType === 'complex') {
      return [...candidates].sort((a, b) => b.performance.accuracy - a.performance.accuracy)[0];
    }
    return [...candidates].sort((a, b) => a.cost - b.cost)[0];
  }

  // ─── Cache helpers
  _cacheKey(prompt, options) {
    return `${options.taskType || 'simple'}:${options.systemPrompt || ''}:${prompt}`.slice(0, 512);
  }

  _getCache(key) {
    const entry = this.cache.get(key);
    if (!entry) return null;
    if (Date.now() - entry.ts > this.cacheTTL) { this.cache.delete(key); return null; }
    return entry.value;
  }

  _setCache(key, value) {
    if (this.cache.size >= this.cacheMaxSize) {
      // Evict oldest entry
      const oldest = this.cache.keys().next().value;
      this.cache.delete(oldest);
    }
    this.cache.set(key, { value, ts: Date.now() });
  }

  // ─── Trimite un prompt cu fallback automat multi-model
  // options: { taskType, systemPrompt, maxTokens, history, excludeNames, useCache }
  async ask(prompt, options = {}) {
    const {
      taskType = 'simple',
      systemPrompt = 'You are Zeus AI, a helpful assistant.',
      maxTokens = 500,
      history = [],
      excludeNames = [],
      useCache = true,
    } = options;

    this.stats.totalCalls++;

    // Cache lookup (skip pentru search – rezultatele trebuie să fie recente)
    if (useCache && taskType !== 'search') {
      const ck = this._cacheKey(prompt, options);
      const cached = this._getCache(ck);
      if (cached) { this.stats.cacheHits++; return cached; }
    }

    const model = this.selectModel(taskType, excludeNames);
    if (!model) {
      throw new Error('[UAIC] Niciun model AI disponibil. Setează cel puțin o cheie API.');
    }

    const t0 = Date.now();
    try {
      const text = await this._callModel(model, prompt, { systemPrompt, maxTokens, history });
      const latency = Date.now() - t0;
      this.stats.callsByModel[model.name] = (this.stats.callsByModel[model.name] || 0) + 1;
      // Medie rulantă latență
      const prev = this.stats.latencyMs[model.name] || latency;
      this.stats.latencyMs[model.name] = Math.round((prev * 0.8) + (latency * 0.2));

      const result = { text, model: model.name, latencyMs: latency };

      if (useCache && taskType !== 'search') {
        this._setCache(this._cacheKey(prompt, options), result);
      }
      return result;
    } catch (err) {
      this.stats.errors[model.name] = (this.stats.errors[model.name] || 0) + 1;
      console.warn(`[UAIC] ${model.name} a eșuat (${Date.now() - t0}ms): ${err.message}. Fallback...`);

      const newExclude = [...excludeNames, model.name];
      if (newExclude.length < this.models.length) {
        return this.ask(prompt, { ...options, excludeNames: newExclude });
      }
      throw new Error(`[UAIC] Toate modelele au eșuat. Ultima eroare: ${err.message}`);
    }
  }

  // ─── Apelant intern per tip de provider
  async _callModel(model, prompt, { systemPrompt, maxTokens, history }) {
    const messages = [
      { role: 'system', content: systemPrompt },
      ...history.slice(-6).map(m => ({ role: m.role, content: String(m.content) })),
      { role: 'user', content: String(prompt) },
    ];
    const headers = {
      Authorization: `Bearer ${model.apiKey}`,
      'Content-Type': 'application/json',
      ...(model.extraHeaders || {}),
    };

    // ── OpenAI-compatible (DeepSeek, Mistral, Groq, OpenRouter, Perplexity,
    //    Together, Fireworks, SambaNova, NVIDIA NIM, xAI Grok, OpenAI) ──────
    if (model.type === 'openai-compat') {
      const res = await axios.post(model.endpoint, {
        model: model.model,
        messages,
        max_tokens: maxTokens,
        temperature: 0.7,
      }, { headers, timeout: model.timeout || 25000 });
      return res.data.choices[0].message.content;
    }

    // ── Anthropic Claude ──────────────────────────────────────────────────
    if (model.type === 'anthropic') {
      const res = await axios.post(model.endpoint, {
        model: model.model,
        max_tokens: maxTokens,
        system: systemPrompt,
        messages: [
          ...history.slice(-6).map(m => ({ role: m.role, content: String(m.content) })),
          { role: 'user', content: String(prompt) },
        ],
      }, {
        headers: {
          'x-api-key': model.apiKey,
          'anthropic-version': '2023-06-01',
          'Content-Type': 'application/json',
        },
        timeout: model.timeout || 30000,
      });
      return res.data.content[0].text;
    }

    // ── Google Gemini ─────────────────────────────────────────────────────
    if (model.type === 'google') {
      const contents = [
        ...history.slice(-6).map(m => ({
          role: m.role === 'assistant' ? 'model' : 'user',
          parts: [{ text: String(m.content) }],
        })),
        { role: 'user', parts: [{ text: String(prompt) }] },
      ];
      const res = await axios.post(`${model.endpoint}?key=${model.apiKey}`, {
        system_instruction: { parts: [{ text: systemPrompt }] },
        contents,
        generationConfig: { maxOutputTokens: maxTokens, temperature: 0.7 },
      }, {
        headers: { 'Content-Type': 'application/json' },
        timeout: model.timeout || 25000,
      });
      return res.data.candidates[0].content.parts[0].text;
    }

    // ── Cohere ────────────────────────────────────────────────────────────
    if (model.type === 'cohere') {
      const chatHistory = history.slice(-6).map(m => ({
        role: m.role === 'assistant' ? 'CHATBOT' : 'USER',
        message: String(m.content),
      }));
      const res = await axios.post(model.endpoint, {
        model: model.model,
        preamble: systemPrompt,
        chat_history: chatHistory,
        message: String(prompt),
        max_tokens: maxTokens,
        temperature: 0.7,
      }, {
        headers: { Authorization: `Bearer ${model.apiKey}`, 'Content-Type': 'application/json' },
        timeout: model.timeout || 25000,
      });
      return res.data.text;
    }

    // ── HuggingFace Inference API ─────────────────────────────────────────
    if (model.type === 'huggingface') {
      const res = await axios.post(model.endpoint, {
        inputs: `<s>[INST] ${systemPrompt}\n${prompt} [/INST]`,
        parameters: { max_new_tokens: maxTokens, temperature: 0.7, return_full_text: false },
      }, {
        headers: { Authorization: `Bearer ${model.apiKey}`, 'Content-Type': 'application/json' },
        timeout: model.timeout || 40000,
      });
      const output = Array.isArray(res.data) ? res.data[0]?.generated_text : res.data?.generated_text;
      return output || '';
    }

    throw new Error(`[UAIC] Tip de provider necunoscut: ${model.type}`);
  }

  // ─── Test individual provider connectivity
  async testProvider(providerName) {
    const model = this.models.find(m => m.name === providerName);
    if (!model) return { ok: false, reason: 'provider_not_active' };
    const t0 = Date.now();
    try {
      const text = await this._callModel(model, 'Reply with: OK', {
        systemPrompt: 'You are a test agent.',
        maxTokens: 10,
        history: [],
      });
      return { ok: true, latencyMs: Date.now() - t0, response: text.trim().slice(0, 50) };
    } catch (err) {
      return { ok: false, latencyMs: Date.now() - t0, error: err.message };
    }
  }

  // ─── Router Express pentru rute admin /api/uaic/*
  getRouter(secretMiddleware) {
    const router = require('express').Router();
    router.use(secretMiddleware);

    router.get('/models', (req, res) => {
      res.json({ models: this.getModels(), count: this.models.length });
    });

    router.get('/stats', (req, res) => {
      res.json(this.getStats());
    });

    router.get('/status', (req, res) => {
      res.json(this.getStatus());
    });

    router.post('/reload', (req, res) => {
      this._loadActiveModels();
      res.json({ success: true, modelsLoaded: this.models.length });
    });

    router.post('/test/:provider', async (req, res) => {
      const result = await this.testProvider(req.params.provider);
      res.json(result);
    });

    router.post('/test-all', async (req, res) => {
      const results = {};
      await Promise.allSettled(
        this.models.map(async m => {
          results[m.name] = await this.testProvider(m.name);
        })
      );
      res.json(results);
    });

    return router;
  }
}

