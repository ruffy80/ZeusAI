function detectStaticPath(unicornRoot) {
  const candidates = [
    process.env.SITE_STATIC_PATH,
    path.join(unicornRoot, 'public'),
    path.join(unicornRoot, 'src', 'site'),
    path.join(unicornRoot, 'client'),
  ].filter(Boolean);
  for (const c of candidates) {
    if (fs.existsSync(c)) return c;
  }
  // Fallback: create public/
  const fallback = path.join(unicornRoot, 'public');
  fs.mkdirSync(fallback, { recursive: true });
  return fallback;
}

