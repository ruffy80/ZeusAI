function completeSite({ unicornRoot, dryRun = false } = {}) {
  const root = unicornRoot || path.resolve(__dirname, '../../..');
  const staticPath = detectStaticPath(root);
  const written = [];
  const skipped = [];

  for (const page of REQUIRED_PAGES) {
    const target = path.join(staticPath, page);
    if (fs.existsSync(target)) { skipped.push(page); continue; }
    const html = pageTemplate(
      page.replace('.html', '').replace(/^./, (c) => c.toUpperCase()),
      PAGE_BODIES[page] ? PAGE_BODIES[page]() : '',
    );
    if (!dryRun) fs.writeFileSync(target, html);
    written.push(page);
  }

  // Bridge JS — overwrite-safe (small, read-only client). Skip if exists.
  const bridgePath = path.join(staticPath, 'zac-bridge.js');
  if (!fs.existsSync(bridgePath)) {
    if (!dryRun) fs.writeFileSync(bridgePath, ZAC_BRIDGE_JS);
    written.push('zac-bridge.js');
  } else {
    skipped.push('zac-bridge.js');
  }

  return { staticPath, written, skipped, dryRun };
}

module.exports = { completeSite, detectStaticPath, REQUIRED_PAGES };
