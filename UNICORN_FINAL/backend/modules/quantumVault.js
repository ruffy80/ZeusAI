// quantumVault.js – Ultra-secure secret storage with hardware fingerprint and auto-destruct
'use strict';

const crypto = require('crypto');
const os = require('os');

// Generate a hardware fingerprint (combination of stable machine identifiers)
function getHardwareFingerprint() {
  const net = os.networkInterfaces();
  const macs = Object.values(net).flat()
    .filter(i => i && !i.internal && i.mac && i.mac !== '00:00:00:00:00:00')
    .map(i => i.mac)
    .sort()
    .join('|');
  const cpus = os.cpus().map(c => c.model).join('|');
  const hostname = os.hostname();
  const platform = os.platform() + os.arch();
  return crypto.createHash('sha256').update(`${macs}|${cpus}|${hostname}|${platform}`).digest('hex');
}

const HARDWARE_FP = getHardwareFingerprint();
let failedAttempts = 0;
const MAX_ATTEMPTS = 10;
let vaultLocked = false;
const vaultStore = new Map(); // key → encrypted value

function deriveVaultKey(fingerprint, masterSecret) {
  return crypto.pbkdf2Sync(
    masterSecret || process.env.VAULT_MASTER_SECRET || 'quantum-vault-default',
    fingerprint,
    100000, 32, 'sha256'
  );
}

function encryptSecret(value, key) {
  const iv = crypto.randomBytes(16);
  const cipher = crypto.createCipheriv('aes-256-gcm', key, iv);
  const encrypted = Buffer.concat([cipher.update(String(value), 'utf8'), cipher.final()]);
  const tag = cipher.getAuthTag();
  return {
    iv: iv.toString('hex'),
    data: encrypted.toString('hex'),
    tag: tag.toString('hex'),
  };
}

function decryptSecret(stored, key) {
  const iv = Buffer.from(stored.iv, 'hex');
  const data = Buffer.from(stored.data, 'hex');
  const tag = Buffer.from(stored.tag, 'hex');
  const decipher = crypto.createDecipheriv('aes-256-gcm', key, iv);
  decipher.setAuthTag(tag);
  return Buffer.concat([decipher.update(data), decipher.final()]).toString('utf8');
}

function checkVaultAccess() {
  if (vaultLocked) throw new Error('[QuantumVault] VAULT LOCKED – Maximum tamper attempts exceeded.');
}

function store(key, value, masterSecret) {
  checkVaultAccess();
  const vaultKey = deriveVaultKey(HARDWARE_FP, masterSecret);
  vaultStore.set(key, encryptSecret(value, vaultKey));
  console.log(`[QuantumVault] Secret stored: ${key}`);
}

function retrieve(key, masterSecret) {
  checkVaultAccess();
  failedAttempts = 0; // successful access resets counter

  const stored = vaultStore.get(key);
  if (!stored) {
    // Also check environment variables as fallback
    return process.env[key] || null;
  }

  try {
    const vaultKey = deriveVaultKey(HARDWARE_FP, masterSecret);
    return decryptSecret(stored, vaultKey);
  } catch (e) {
    failedAttempts++;
    console.error(`[QuantumVault] Decryption failed for ${key}. Attempts: ${failedAttempts}/${MAX_ATTEMPTS}`);
    if (failedAttempts >= MAX_ATTEMPTS) {
      vaultLocked = true;
      console.error('[QuantumVault] 🚨 AUTO-DESTRUCT: Vault locked after maximum tamper attempts!');
      // In production: wipe sensitive data
      vaultStore.clear();
    }
    throw new Error('Vault access denied');
  }
}

function remove(key) {
  checkVaultAccess();
  vaultStore.delete(key);
}

function listKeys() {
  checkVaultAccess();
  return Array.from(vaultStore.keys());
}

function unlock(emergencyCode) {
  const emergencySecret = process.env.VAULT_EMERGENCY_CODE;
  if (!emergencySecret) {
    console.error('[QuantumVault] VAULT_EMERGENCY_CODE not configured – emergency unlock disabled.');
    return false;
  }
  const expected = crypto.createHash('sha256')
    .update(HARDWARE_FP + emergencySecret)
    .digest('hex');
  let inputBuf, expectedBuf;
  try {
    inputBuf = Buffer.from(emergencyCode, 'hex');
    expectedBuf = Buffer.from(expected, 'hex');
  } catch {
    return false;
  }
  if (inputBuf.length !== expectedBuf.length) return false;
  if (crypto.timingSafeEqual(inputBuf, expectedBuf)) {
    vaultLocked = false;
    failedAttempts = 0;
    console.log('[QuantumVault] Vault unlocked via emergency code.');
    return true;
  }
  return false;
}

function getStatus() {
  return {
    locked: vaultLocked,
    failedAttempts,
    maxAttempts: MAX_ATTEMPTS,
    storedSecrets: vaultStore.size,
    hardwareFingerprint: HARDWARE_FP.slice(0, 16) + '...',
    environment: process.env.NODE_ENV || 'development',
  };
}

