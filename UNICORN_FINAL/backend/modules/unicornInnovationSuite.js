const crypto = require('crypto');

class UnicornInnovationSuite {
  constructor() {
    this.cache = new Map(); this.cacheTTL = 60000; 
    this.trust = {
      status: 'operational',
      uptime: 99.97,
      sla: '99.9%',
      incidents: [],
      audits: []
    };

    this.billing = {
      plans: [
        { id: 'starter', name: 'Starter', priceMonthly: 49, limits: { apiCalls: 10000, seats: 3 } },
        { id: 'pro', name: 'Pro', priceMonthly: 199, limits: { apiCalls: 120000, seats: 15 } },
        { id: 'enterprise', name: 'Enterprise', priceMonthly: 999, limits: { apiCalls: 1500000, seats: 100 } }
      ],
      subscriptions: new Map(),
      usage: new Map()
    };

    this.apiKeys = new Map();
    this.webhooks = [];
    this.marketSignals = [];
    this.experiments = [];
    this.security = { sessions: [], devices: [] };
    this.onboarding = new Map();
    this.caseStudies = [
      { id: 'cs-1', company: 'AeroNova', impact: '+31% eficiență operațională', timelineDays: 45 },
      { id: 'cs-2', company: 'FinPulse', impact: '+22% conversie', timelineDays: 28 },
      { id: 'cs-3', company: 'GovMesh', impact: '-41% timp procesare', timelineDays: 60 }
    ];
    this.referrals = new Map();
  }

  // 1) Trust Center
  getTrustStatus() {
    return {
      status: this.trust.status,
      uptime: this.trust.uptime,
      sla: this.trust.sla,
      openIncidents: this.trust.incidents.filter(i => i.status !== 'resolved').length,
      timestamp: new Date().toISOString()
    };
  }

  getIncidents() {
    return this.trust.incidents;
  }

  getAuditTrail() {
    return this.trust.audits.slice(-100);
  }

  addAudit(event, actor = 'system') {
    this.trust.audits.push({ id: crypto.randomUUID(), event, actor, at: new Date().toISOString() });
  }

  // 2) Usage billing
  getPlans() {
    return this.billing.plans;
  }

  subscribe({ clientId, planId }) {
    const plan = this.billing.plans.find(p => p.id === planId);
    if (!plan) throw new Error('Plan invalid');
    this.billing.subscriptions.set(clientId, { clientId, planId, since: new Date().toISOString() });
    if (!this.billing.usage.has(clientId)) {
      this.billing.usage.set(clientId, { apiCalls: 0, seats: 1, storageGb: 1, updatedAt: new Date().toISOString() });
    }
    this.addAudit(`Subscription updated: ${clientId} -> ${planId}`, 'billing');
    return { success: true, plan };
  }

  trackUsage(clientId, delta = {}) {
    const current = this.billing.usage.get(clientId) || { apiCalls: 0, seats: 1, storageGb: 1 };
    const next = {
      apiCalls: Math.max(0, current.apiCalls + Number(delta.apiCalls || 0)),
      seats: Math.max(1, current.seats + Number(delta.seats || 0)),
      storageGb: Math.max(1, current.storageGb + Number(delta.storageGb || 0)),
      updatedAt: new Date().toISOString()
    };
    this.billing.usage.set(clientId, next);
    return next;
  }

  getUsage(clientId) {
    return this.billing.usage.get(clientId) || { apiCalls: 0, seats: 1, storageGb: 1, updatedAt: null };
  }

  buildInvoice(clientId) {
    const sub = this.billing.subscriptions.get(clientId) || { planId: 'starter' };
    const plan = this.billing.plans.find(p => p.id === sub.planId) || this.billing.plans[0];
    const usage = this.getUsage(clientId);

    const overageApi = Math.max(0, usage.apiCalls - plan.limits.apiCalls);
    const overageSeats = Math.max(0, usage.seats - plan.limits.seats);
    const overageTotal = overageApi * 0.0002 + overageSeats * 9;
    const total = Number((plan.priceMonthly + overageTotal).toFixed(2));

    return {
      invoiceId: `INV-${Date.now()}`,
      clientId,
      plan: plan.name,
      base: plan.priceMonthly,
      overage: Number(overageTotal.toFixed(2)),
      total,
      issuedAt: new Date().toISOString()
    };
  }

  // 3) API keys / webhooks
  createApiKey({ owner, scopes = ['read'] }) {
    const id = crypto.randomUUID();
    const token = 'uk_' + crypto.randomBytes(18).toString('hex');
    const key = { id, owner, scopes, tokenMasked: token.slice(0, 8) + '...' + token.slice(-4), createdAt: new Date().toISOString(), active: true, rateLimitPerMin: 120 };
    this.apiKeys.set(id, { ...key, token });
    this.addAudit(`API key created for ${owner}`, 'api-platform');
    return { ...key, token };
  }

  listApiKeys() {
    return Array.from(this.apiKeys.values()).map(k => ({ id: k.id, owner: k.owner, scopes: k.scopes, active: k.active, tokenMasked: k.tokenMasked, rateLimitPerMin: k.rateLimitPerMin }));
  }

  registerWebhook({ url, events = [] }) {
    const hook = { id: crypto.randomUUID(), url, events, createdAt: new Date().toISOString(), deliveries: 0 };
    this.webhooks.push(hook);
    this.addAudit(`Webhook registered: ${url}`, 'api-platform');
    return hook;
  }

  triggerWebhookTest(eventName = 'test.event') {
    this.webhooks = this.webhooks.map(w => ({ ...w, deliveries: w.deliveries + 1, lastEvent: eventName, lastDeliveryAt: new Date().toISOString() }));
    return { success: true, receivers: this.webhooks.length, event: eventName };
  }

  // 4) Marketplace intelligence
  getMarketplaceIntelligence(clientId = 'anonymous') {
    const score = Math.min(100, 50 + Math.floor(Math.random() * 45));
    const result = {
      clientId,
      score,
      roiIndex: Number((1 + score / 100).toFixed(2)),
      risk: score > 80 ? 'low' : score > 65 ? 'medium' : 'high',
      recommendations: [
        'Prioritizează pachetele cu ROI > 1.4',
        'Activează auto-upgrade pe servicii critice',
        'Folosește bundle Enterprise+Security pentru reducere risc'
      ],
      generatedAt: new Date().toISOString()
    };
    this.marketSignals.push(result);
    return result;
  }

  // 5) Experiment engine
  createExperiment({ name, variantA, variantB, goal = 'conversion' }) {
    const exp = {
      id: crypto.randomUUID(),
      name,
      variantA,
      variantB,
      goal,
      createdAt: new Date().toISOString(),
      status: 'running',
      results: { a: 0, b: 0, winner: null }
    };
    this.experiments.push(exp);
    this.addAudit(`Experiment created: ${name}`, 'growth');
    return exp;
  }

  evaluateExperiment(id) {
    const exp = this.experiments.find(e => e.id === id);
    if (!exp) throw new Error('Experiment not found');
    exp.results.a = Math.floor(100 + Math.random() * 400);
    exp.results.b = Math.floor(100 + Math.random() * 400);
    exp.results.winner = exp.results.a >= exp.results.b ? 'A' : 'B';
    exp.status = 'evaluated';
    return exp;
  }

  listExperiments() {
    return this.experiments;
  }

  // 6) Executive copilot
  askCopilot({ prompt = '' }) {
    const normalized = String(prompt).toLowerCase();
    let response = 'Recomand monitorizarea KPI-urilor și optimizare incrementală.';

    if (normalized.includes('pricing') || normalized.includes('pret')) {
      response = 'Ajustează prețul planului Pro cu +8% și oferă discount anual 12% pentru retenție.';
    } else if (normalized.includes('growth') || normalized.includes('crestere')) {
      response = 'Lansează 2 experimente A/B pe onboarding și prioritizează canalele cu CAC sub target.';
    } else if (normalized.includes('risk') || normalized.includes('secur')) {
      response = 'Activează limitare sesiuni pe device și audit zilnic al acțiunilor administrative.';
    }

    return {
      answer: response,
      confidence: 0.86,
      at: new Date().toISOString()
    };
  }

  // 7) Security hardening helpers
  registerSession({ tokenHint = '', device = 'unknown' }) {
    const session = { id: crypto.randomUUID(), tokenHint, device, createdAt: new Date().toISOString(), active: true };
    this.security.sessions.push(session);
    return session;
  }

  listSessions() {
    return this.security.sessions;
  }

  revokeSession(sessionId) {
    const s = this.security.sessions.find(x => x.id === sessionId);
    if (!s) throw new Error('Session not found');
    s.active = false;
    s.revokedAt = new Date().toISOString();
    this.addAudit(`Session revoked: ${sessionId}`, 'security');
    return s;
  }

  checkDevice({ fingerprint = '' }) {
    const known = this.security.devices.includes(fingerprint);
    if (!known && fingerprint) this.security.devices.push(fingerprint);
    return { known, fingerprint, risk: known ? 'low' : 'medium' };
  }

  // 8) Onboarding wizard
  startOnboarding({ company, objective, industry }) {
    const id = crypto.randomUUID();
    const flow = { id, company, objective, industry, createdAt: new Date().toISOString() };
    this.onboarding.set(id, flow);
    return flow;
  }

  getOnboardingRecommendations(id) {
    const flow = this.onboarding.get(id);
    if (!flow) throw new Error('Onboarding not found');
    return {
      id,
      stack: ['Trust Center', 'API Platform', 'Executive Dashboard'],
      suggestedPlan: flow.industry === 'enterprise' ? 'enterprise' : 'pro',
      estimatedROI: '1.6x in 90 days'
    };
  }

  // 9) Case studies + ROI
  getCaseStudies() {
    return this.caseStudies;
  }

  calculateROI({ investment = 0, expectedGain = 0 }) {
    const inv = Number(investment || 0);
    const gain = Number(expectedGain || 0);
    if (inv <= 0) return { roi: 0, paybackMonths: null };
    const roi = (gain - inv) / inv;
    const monthlyNet = Math.max(1, gain / 12);
    return {
      roi: Number(roi.toFixed(2)),
      roiPercent: Number((roi * 100).toFixed(2)),
      paybackMonths: Number((inv / monthlyNet).toFixed(1))
    };
  }

  // 10) Affiliate layer
  createReferral({ partnerName }) {
    const code = 'UNI-' + crypto.randomBytes(4).toString('hex').toUpperCase();
    const data = { code, partnerName, clicks: 0, conversions: 0, commissionUSD: 0, createdAt: new Date().toISOString() };
    this.referrals.set(code, data);
    return data;
  }

  getReferral(code) {
    const r = this.referrals.get(code);
    if (!r) throw new Error('Referral not found');
    return r;
  }

  getAffiliateStats() {
    const all = Array.from(this.referrals.values());
    return {
      partners: all.length,
      clicks: all.reduce((a, b) => a + b.clicks, 0),
      conversions: all.reduce((a, b) => a + b.conversions, 0),
      commissionUSD: all.reduce((a, b) => a + b.commissionUSD, 0)
    };
  }
}

module.exports = new UnicornInnovationSuite();
