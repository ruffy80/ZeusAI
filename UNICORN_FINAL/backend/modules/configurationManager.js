// configurationManager.js – Encrypted secrets management, initial wizard, DNS integration, backup
'use strict';

const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const CONFIG_FILE = path.join(__dirname, '../../.config.encrypted');
const BACKUP_DIR = path.join(__dirname, '../../backups/config');

let memoryStore = {};
let initialized = false;

function deriveKey(secret) {
  return crypto.createHash('sha256').update(secret || 'default-unicorn-key').digest();
}

function encrypt(text, secret) {
  const key = deriveKey(secret);
  const iv = crypto.randomBytes(16);
  const cipher = crypto.createCipheriv('aes-256-cbc', key, iv);
  const encrypted = Buffer.concat([cipher.update(text, 'utf8'), cipher.final()]);
  return iv.toString('hex') + ':' + encrypted.toString('hex');
}

function decrypt(data, secret) {
  const [ivHex, encHex] = data.split(':');
  const key = deriveKey(secret);
  const iv = Buffer.from(ivHex, 'hex');
  const enc = Buffer.from(encHex, 'hex');
  const decipher = crypto.createDecipheriv('aes-256-cbc', key, iv);
  return Buffer.concat([decipher.update(enc), decipher.final()]).toString('utf8');
}

function loadConfig() {
  const masterSecret = process.env.MASTER_CONFIG_SECRET || 'unicorn-config-secret';
  if (fs.existsSync(CONFIG_FILE)) {
    try {
      const raw = fs.readFileSync(CONFIG_FILE, 'utf8');
      memoryStore = JSON.parse(decrypt(raw, masterSecret));
      console.log('[ConfigManager] Configuration loaded from encrypted store.');
    } catch (e) {
      console.warn('[ConfigManager] Could not decrypt config file:', e.message);
    }
  }

  // Merge environment variables
  const envKeys = [
    'OPENAI_API_KEY', 'DEEPSEEK_API_KEY', 'GITHUB_TOKEN', 'VERCEL_TOKEN',
    'HETZNER_HOST', 'HETZNER_USER', 'DOMAIN', 'SAV_API_TOKEN', 'ADMIN_SECRET',
    'BTC_WALLET_ADDRESS', 'JWT_SECRET', 'STRIPE_SECRET_KEY', 'WEBHOOK_SECRET',
  ];
  envKeys.forEach(k => {
    if (process.env[k]) memoryStore[k] = process.env[k];
  });

  initialized = true;
}

function saveConfig() {
  const masterSecret = process.env.MASTER_CONFIG_SECRET || 'unicorn-config-secret';
  try {
    fs.mkdirSync(path.dirname(CONFIG_FILE), { recursive: true });
    const encrypted = encrypt(JSON.stringify(memoryStore), masterSecret);
    fs.writeFileSync(CONFIG_FILE, encrypted, 'utf8');
    console.log('[ConfigManager] Configuration saved (encrypted).');
  } catch (e) {
    console.error('[ConfigManager] Failed to save config:', e.message);
  }
}

function get(key, fallback = null) {
  if (!initialized) loadConfig();
  return memoryStore[key] ?? process.env[key] ?? fallback;
}

function set(key, value) {
  memoryStore[key] = value;
  saveConfig();
}

function backup() {
  try {
    fs.mkdirSync(BACKUP_DIR, { recursive: true });
    const ts = new Date().toISOString().replace(/[:.]/g, '-');
    const backupFile = path.join(BACKUP_DIR, `config-backup-${ts}.enc`);
    if (fs.existsSync(CONFIG_FILE)) {
      fs.copyFileSync(CONFIG_FILE, backupFile);
      // Keep last 5 backups
      const files = fs.readdirSync(BACKUP_DIR).sort();
      if (files.length > 5) {
        files.slice(0, files.length - 5).forEach(f => {
          try { fs.unlinkSync(path.join(BACKUP_DIR, f)); } catch {}
        });
      }
      console.log('[ConfigManager] Config backup created:', backupFile);
    }
  } catch (e) {
    console.error('[ConfigManager] Backup failed:', e.message);
  }
}

function runWizard(overrides = {}) {
  const required = ['DOMAIN', 'ADMIN_SECRET', 'BTC_WALLET_ADDRESS'];
  const missing = required.filter(k => !get(k) && !overrides[k]);
  if (missing.length > 0) {
    console.warn('[ConfigManager] Missing required config keys:', missing.join(', '));
    console.warn('[ConfigManager] Run wizard or set environment variables.');
  }
  Object.entries(overrides).forEach(([k, v]) => set(k, v));
  return { missing, configured: Object.keys(memoryStore).length };
}

function getStatus() {
  return {
    initialized,
    keysLoaded: Object.keys(memoryStore).length,
    encryptedFileExists: fs.existsSync(CONFIG_FILE),
    domain: get('DOMAIN', '—'),
    hasBtcWallet: !!get('BTC_WALLET_ADDRESS'),
    hasAdminSecret: !!get('ADMIN_SECRET'),
  };
}

// Auto-load on require
loadConfig();

module.exports = { get, set, backup, runWizard, getStatus, loadConfig, saveConfig };
