function saveConfig() {
  const masterSecret = process.env.MASTER_CONFIG_SECRET || 'unicorn-config-secret';
  try {
    fs.mkdirSync(path.dirname(CONFIG_FILE), { recursive: true });
    const encrypted = encrypt(JSON.stringify(memoryStore), masterSecret);
    fs.writeFileSync(CONFIG_FILE, encrypted, 'utf8');
    console.log('[ConfigManager] Configuration saved (encrypted).');
  } catch (e) {
    console.error('[ConfigManager] Failed to save config:', e.message);
  }
}

function get(key, fallback = null) {
  if (!initialized) loadConfig();
  return memoryStore[key] ?? process.env[key] ?? fallback;
}

function set(key, value) {
  memoryStore[key] = value;
  saveConfig();
}

function backup() {
  try {
    fs.mkdirSync(BACKUP_DIR, { recursive: true });
    const ts = new Date().toISOString().replace(/[:.]/g, '-');
    const backupFile = path.join(BACKUP_DIR, `config-backup-${ts}.enc`);
    if (fs.existsSync(CONFIG_FILE)) {
      fs.copyFileSync(CONFIG_FILE, backupFile);
      // Keep last 5 backups
      const files = fs.readdirSync(BACKUP_DIR).sort();
      if (files.length > 5) {
        files.slice(0, files.length - 5).forEach(f => {
          try { fs.unlinkSync(path.join(BACKUP_DIR, f)); } catch {}
        });
      }
      console.log('[ConfigManager] Config backup created:', backupFile);
    }
  } catch (e) {
    console.error('[ConfigManager] Backup failed:', e.message);
  }
}

function runWizard(overrides = {}) {
  const required = ['DOMAIN', 'ADMIN_SECRET', 'BTC_WALLET_ADDRESS'];
  const missing = required.filter(k => !get(k) && !overrides[k]);
  if (missing.length > 0) {
    console.warn('[ConfigManager] Missing required config keys:', missing.join(', '));
    console.warn('[ConfigManager] Run wizard or set environment variables.');
  }
  Object.entries(overrides).forEach(([k, v]) => set(k, v));
  return { missing, configured: Object.keys(memoryStore).length };
}

function getStatus() {
  return {
    initialized,
    keysLoaded: Object.keys(memoryStore).length,
    encryptedFileExists: fs.existsSync(CONFIG_FILE),
    domain: get('DOMAIN', '—'),
    hasBtcWallet: !!get('BTC_WALLET_ADDRESS'),
    hasAdminSecret: !!get('ADMIN_SECRET'),
  };
}

// Injectează toate valorile din memoryStore înapoi în process.env dacă lipsesc
function injectToEnv() {
  if (!initialized) loadConfig();
  let injected = 0;
  Object.entries(memoryStore).forEach(([k, v]) => {
    if (!process.env[k] && v) {
      process.env[k] = String(v);
      injected++;
    }
  });
  if (injected > 0) {
    console.log(`[ConfigManager] Injected ${injected} config values into process.env.`);
  }
  return injected;
}

