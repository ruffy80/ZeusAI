'use strict';

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

require('dotenv').config({ path: path.join(__dirname, '../../.env') });
require('dotenv').config({ path: path.join(__dirname, '../../../.env.auto-connector') });

class UnicornAutoGenesis {
  constructor() {
    this.cache = new Map(); this.cacheTTL = 60000; 
    this.repo = process.env.GITHUB_REPO_FULL || `${process.env.GITHUB_OWNER || 'ruffy80'}/${process.env.GITHUB_REPO || 'ZeusAI'}`;
    this.branch = process.env.BRANCH || 'main';
    this.vercelToken = process.env.VERCEL_TOKEN || '';
    this.vercelOrgId = process.env.VERCEL_ORG_ID || process.env.VERCEL_TEAM_ID || '';
    this.vercelProjectId = process.env.VERCEL_PROJECT_ID || '';
    this.hetznerHost = process.env.HETZNER_HOST || '';
    this.hetznerUser = process.env.HETZNER_USER || process.env.HETZNER_DEPLOY_USER || 'root';
    this.hetznerPassword = process.env.HETZNER_PASSWORD || '';
    this.deployPath = process.env.HETZNER_DEPLOY_PATH || '/root/unicorn-final';
    this.webhookSecret = process.env.WEBHOOK_SECRET || process.env.HETZNER_WEBHOOK_SECRET || '';
    this.sshKeyPath = process.env.SSH_KEY_PATH || path.join(process.env.HOME || '', '.ssh/hetzner_rsa');
  }

  runCmd(cmd, silent = false) {
    const out = execSync(cmd, {
      stdio: silent ? 'pipe' : 'inherit',
      shell: '/bin/zsh',
      env: process.env,
    });
    return out ? out.toString() : '';
  }

  ensure(cmd, message) {
    try {
      this.runCmd(cmd, true);
      return true;
    } catch {
      console.error(`❌ ${message}`);
      return false;
    }
  }

  setGhSecret(name, value) {
    if (!value) return;
    const escaped = String(value).replace(/'/g, `'"'"'`);
    this.runCmd(`gh secret set ${name} --repo ${this.repo} --body '${escaped}'`);
  }

  installPublicKeyOnHetzner() {
    if (!this.hetznerPassword || !this.hetznerHost) return;
    if (!fs.existsSync(this.sshKeyPath) || !fs.existsSync(`${this.sshKeyPath}.pub`)) return;

    const pub = fs.readFileSync(`${this.sshKeyPath}.pub`, 'utf8').trim();
    const pubEscaped = pub.replace(/'/g, `'"'"'`);
    const passEscaped = this.hetznerPassword.replace(/'/g, `'"'"'`);

    this.runCmd(
      `SSHPASS='${passEscaped}' sshpass -e ssh -o StrictHostKeyChecking=no -o PreferredAuthentications=password -o PubkeyAuthentication=no ${this.hetznerUser}@${this.hetznerHost} "mkdir -p ~/.ssh && chmod 700 ~/.ssh && touch ~/.ssh/authorized_keys && grep -qxF '${pubEscaped}' ~/.ssh/authorized_keys || echo '${pubEscaped}' >> ~/.ssh/authorized_keys && chmod 600 ~/.ssh/authorized_keys"`
    );
  }

  async run() {
    console.log(`
╔══════════════════════════════════════════════════════════════════╗
║   🦄 UNICORN AUTO‑GENESIS (SAFE MODE)                           ║
║   Configurează GitHub + Vercel + Hetzner fără git rewrite       ║
╚══════════════════════════════════════════════════════════════════╝
`);

    if (!this.ensure('command -v gh', 'GitHub CLI (gh) nu este instalat')) return;
    if (!this.ensure('gh auth status', 'Nu ești autentificat în gh')) {
      const token = this.runCmd('gh auth token || true', true).trim();
      if (!token) return;
      console.log('ℹ️ gh auth status a eșuat în non-interactive mode, folosesc token activ.');
    }

    console.log('🔧 Verific credențiale de bază...');
    if (!this.vercelToken || !this.vercelOrgId || !this.vercelProjectId) {
      throw new Error('Lipsesc variabilele VERCEL_TOKEN / VERCEL_ORG_ID / VERCEL_PROJECT_ID');
    }
    if (!this.hetznerHost) {
      throw new Error('Lipsește HETZNER_HOST');
    }

    if (this.ensure(`curl -fsS -H "Authorization: Bearer ${this.vercelToken}" https://api.vercel.com/v2/user`, true)) {
      console.log('✅ Vercel token valid');
    } else {
      throw new Error('VERCEL_TOKEN invalid');
    }

    console.log('🔧 Setez secrete GitHub Actions...');
    this.setGhSecret('VERCEL_TOKEN', this.vercelToken);
    this.setGhSecret('VERCEL_ORG_ID', this.vercelOrgId);
    this.setGhSecret('VERCEL_PROJECT_ID', this.vercelProjectId);
    this.setGhSecret('VERCEL_TEAM_ID', this.vercelOrgId);

    this.setGhSecret('HETZNER_HOST', this.hetznerHost);
    this.setGhSecret('HETZNER_USER', this.hetznerUser);
    this.setGhSecret('HETZNER_DEPLOY_USER', this.hetznerUser);
    this.setGhSecret('HETZNER_DEPLOY_PORT', '22');
    this.setGhSecret('HETZNER_DEPLOY_PATH', this.deployPath);
    this.setGhSecret('SSH_HOST', this.hetznerHost);
    this.setGhSecret('SSH_USER', this.hetznerUser);
    this.setGhSecret('SSH_PORT', '22');

    if (fs.existsSync(this.sshKeyPath)) {
      this.setGhSecret('HETZNER_SSH_PRIVATE_KEY', fs.readFileSync(this.sshKeyPath, 'utf8'));
      this.setGhSecret('SSH_PRIVATE_KEY', fs.readFileSync(this.sshKeyPath, 'utf8'));
    }

    const webhookUrl = `http://${this.hetznerHost}:3000/deploy`;
    this.setGhSecret('HETZNER_WEBHOOK_URL', webhookUrl);
    this.setGhSecret('GH_WEBHOOK_URL', webhookUrl);
    this.setGhSecret('WEBHOOK_URL', webhookUrl);
    if (this.webhookSecret) {
      this.setGhSecret('WEBHOOK_SECRET', this.webhookSecret);
      this.setGhSecret('HETZNER_WEBHOOK_SECRET', this.webhookSecret);
    }

    if (this.hetznerPassword) {
      console.log('🔧 Instalez cheia publică pe Hetzner folosind parola root...');
      this.installPublicKeyOnHetzner();
    }

    console.log('🚀 Declanșez deploy workflow...');
    this.runCmd(`gh workflow run vercel-deploy.yml --repo ${this.repo} --ref ${this.branch}`);

    console.log('⏳ Aștept finalizarea...');
    this.runCmd(`sleep 20`);
    const runId = this.runCmd(`gh run list --repo ${this.repo} --workflow vercel-deploy.yml --limit 1 --json databaseId -q '.[0].databaseId'`, true).trim();
    if (runId) {
      this.runCmd(`gh run watch ${runId} --repo ${this.repo} || true`);
      this.runCmd(`gh run view ${runId} --repo ${this.repo} --json status,conclusion`);
    }

    console.log(`
✅ AUTO‑GENESIS finalizat.
🌐 Production: https://zeusai.pro
🖥️ Hetzner: http://${this.hetznerHost}:3000/health
📦 GitHub Actions: https://github.com/${this.repo}/actions
`);
  }
}

module.exports = new UnicornAutoGenesis();
