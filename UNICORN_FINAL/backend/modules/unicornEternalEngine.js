  async runSelfTests(code, resolvedPath) {
    if (resolvedPath.endsWith('.js')) {
      const tempPath = `${resolvedPath}.uee-check.js`;
      fs.writeFileSync(tempPath, code);
      try {
        await new Promise((resolve, reject) => {
          exec(`node --check "${tempPath}"`, (error) => {
            if (error) reject(error);
            else resolve();
          });
        });
        return true;
      } catch {
        return false;
      } finally {
        if (fs.existsSync(tempPath)) fs.unlinkSync(tempPath);
      }
    }
    return true;
  }

