// dynamic-pricing.js – Dynamic pricing engine based on market conditions and client behavior
'use strict';

const BASE_PRICES = {
  free: 0,
  starter: 29,
  pro: 99,
  enterprise: 499,
  'api-call': 0.01,
  'ai-analysis': 5,
  'wealth-engine': 199,
  'legal-bot': 49,
  'cloud-broker': 79,
  'data-export': 9,
  // ── Revenue-tier modules (SME, Mid-Market, Enterprise, Global Giants) ──
  // These IDs power /api/pricing/module/:moduleId so the site can show a
  // real-time, AI-negotiated price per segment. Base prices are the floor;
  // the dynamic-pricing engine then applies demand, peak-hours, surge,
  // discount, per-service variance and (optional) coupon/loyalty.
  sme: 199,
  'mid-market': 1499,
  'enterprise-tier': 9999,
  'global-giants': 99999,
};

const DEMAND_FACTOR_HISTORY = [];
let currentDemandFactor = 1.0;
let peakHoursActive = false;
let surgeActive = false;
let discountActive = true; // 20% discount as per spec

// Per-service noise: stable per (serviceId, hour) so prices differ between
// products but don't flicker on every request. Hash of serviceId + hour bucket
// yields deterministic ±15% variance per service.
const crypto = require('crypto');
function perServiceFactor(serviceId) {
  const hour = Math.floor(Date.now() / 3600000); // hourly bucket
  const h = crypto.createHash('sha256').update(String(serviceId) + ':' + hour).digest();
  // Normalize first 4 bytes to 0..1, then map to 0.85..1.15
  const raw = h.readUInt32BE(0) / 0xffffffff;
  return 0.85 + raw * 0.30;
}

function updateDemandFactor() {
  const hour = new Date().getHours();
  // Peak hours: 9-12 and 14-18 (European business hours)
  peakHoursActive = (hour >= 9 && hour <= 12) || (hour >= 14 && hour <= 18);

  const base = peakHoursActive ? 1.05 : 0.95;
  const noise = (Math.random() - 0.5) * 0.1;
  currentDemandFactor = Math.max(0.7, Math.min(1.5, base + noise));

  DEMAND_FACTOR_HISTORY.push({ factor: currentDemandFactor, ts: new Date().toISOString() });
  if (DEMAND_FACTOR_HISTORY.length > 100) DEMAND_FACTOR_HISTORY.shift();
}

function getPrice(serviceId, options = {}) {
  const base = BASE_PRICES[serviceId] ?? 99;
  const { userId, quantity = 1, coupon } = options;

  const svcFactor = perServiceFactor(serviceId);
  const effectiveDemand = currentDemandFactor * svcFactor;
  let price = base * effectiveDemand;

  // Volume discounts
  if (quantity >= 10) price *= 0.85;
  else if (quantity >= 5) price *= 0.92;

  // Loyalty discount (returning user)
  if (userId) price *= 0.95;

  // Surge pricing
  if (surgeActive) price *= 1.2;

  // Global 20% discount
  if (discountActive) price *= 0.80;

  // Coupon codes
  if (coupon === 'UNICORN2026') price *= 0.7;
  if (coupon === 'LAUNCH50') price *= 0.5;

  const unclampedPrice = Math.round(price * 100) / 100;
  const finalPrice = Math.max(0, Math.min(10000000, unclampedPrice));
  if (finalPrice !== unclampedPrice) {
    try {
      console.warn('[DynamicPricing] unrealistic price clamped', JSON.stringify({ serviceId, unclampedPrice, finalPrice, min: 0, max: 10000000 }));
    } catch (_) {}
  }

  return {
    serviceId,
    basePrice: base,
    finalPrice,
    demandFactor: Math.round(effectiveDemand * 1000) / 1000,
    globalDemand: Math.round(currentDemandFactor * 1000) / 1000,
    serviceFactor: Math.round(svcFactor * 1000) / 1000,
    discountApplied: discountActive,
    surgeActive,
    peakHours: peakHoursActive,
    currency: 'USD',
    btcEquivalent: null, // filled on demand
    quantity,
  };
}

function getAllPrices(options = {}) {
  return Object.keys(BASE_PRICES).reduce((acc, k) => {
    acc[k] = getPrice(k, options);
    return acc;
  }, {});
}

// Allowed surge durations (whitelisted values to prevent resource exhaustion)
const ALLOWED_SURGE_DURATIONS_MS = Object.freeze({
  '30min': 1800000,
  '1h': 3600000,
  '2h': 7200000,
  '6h': 21600000,
  '24h': 86400000,
});

function activateSurge(durationKey = '1h') {
  const durationMs = ALLOWED_SURGE_DURATIONS_MS[durationKey] || ALLOWED_SURGE_DURATIONS_MS['1h'];
  surgeActive = true;
  setTimeout(() => { surgeActive = false; }, durationMs);
  console.log('[DynamicPricing] Surge pricing activated for', durationMs / 60000, 'min');
}

function setDiscount(active) {
  discountActive = active;
}

function getMarketConditions() {
  return {
    demandFactor: currentDemandFactor,
    peakHours: peakHoursActive,
    surgeActive,
    discountActive,
    history: DEMAND_FACTOR_HISTORY.slice(-10),
  };
}

// Update demand factor every 5 minutes
setInterval(updateDemandFactor, 5 * 60 * 1000);
updateDemandFactor(); // initial call

module.exports = { getPrice, getAllPrices, activateSurge, setDiscount, getMarketConditions, BASE_PRICES, ALLOWED_SURGE_DURATIONS_MS };
